<h1>Política de Cookies</h1>
<p>Nuestro sitio web utiliza cookies para mejorar la experiencia del usuario. Al utilizar nuestro sitio web, usted acepta todas las cookies de acuerdo con nuestra Política de Cookies.</p>
<h2>¿Qué son las cookies?</h2>
<p>Las cookies son pequeños archivos de texto que se almacenan en su ordenador o dispositivo móvil cuando visita nuestro sitio web. Las cookies nos permiten reconocer su navegador y, por ejemplo, recordar sus preferencias de usuario como el idioma seleccionado y otras configuraciones.</p>
<h2>¿Cómo utilizamos las cookies?</h2>
<p>Utilizamos cookies para varios propósitos, incluyendo:</p>
<ul>
    <li>Analizar cómo se utiliza nuestro sitio web y mejorar su funcionalidad.</li>
    <li>Personalizar su experiencia de usuario y recordar sus preferencias.</li>
    <li>Proporcionar publicidad personalizada.</li>
</ul>
<h2>¿Cómo controlar las cookies?</h2>
<p>Puede controlar y/o eliminar las cookies según desee. Para obtener más información, consulte aboutcookies.org. Puede eliminar todas las cookies ya almacenadas en su ordenador y configurar la mayoría de los navegadores para que eviten que se almacenen cookies. Sin embargo, en este caso, es posible que tenga que ajustar manualmente algunas preferencias cada vez que visite un sitio web, y algunos servicios y funcionalidades pueden no estar disponibles.</p>
